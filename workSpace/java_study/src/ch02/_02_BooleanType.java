package ch02;

// 논리형 자료형
public class _02_BooleanType {
	public static void main(String[] args) {
		// boolean(1byte) : 어떤 변수의 참, 거짓의 값을 나타내는 데 사용
		boolean Man = false;   // 논리형 타입의 변수 Man의 값을 거짓으로 대입. 
		boolean dog = true;    // 논리형 타입의 변수 dog의 값을 참으로 대입. 
		System.out.println("Man : " + Man);
		System.out.println("dog : " + dog);
	}
}

