package ch09_04;

// [멤버 변수를 public static final로 선언] 
public class Define {
	// public : 외부 클래스에서 참조 가능 
	// static : new 인스턴스 생성 없이, 클래스명으로 참조 가능 
	public static final int MIN = 1;
	public static final int MAX = 9999;
	public static final int ENG = 40;
	public static final int MATH = 70;
	public static final double PI = 3.14;
	public static final String Good_EVENING = "Good Evening";
}
